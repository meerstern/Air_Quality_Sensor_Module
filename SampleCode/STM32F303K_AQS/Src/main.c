  /* USER CODE BEGIN 2 */
  //I2C:SCL,SDA::PB6,PB7::D5,D4
  uint8_t rdata[AQS_DATA_LEN];
  int16_t int_temp,int_humd,int_eth,int_tvoc,int_eco2,int_iaq,int_rcda;
